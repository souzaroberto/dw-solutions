<?php

for ($index=0; $index < 10; $index ++) {
    for ($second=0; $second < 10; $second ++) {
        echo $index.$second.' ';
    }
    echo "\n";
}

echo "\n";

for ($index=9; $index > -1; $index --){
    for ($second=9; $second > -1; $second --) {
        echo $index.$second.' ';
    }
    echo "\n";
}

echo "\n"

?>
